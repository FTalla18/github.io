    const blogPostsContainer = document.getElementById('blog-posts');
    const articleContainer = document.getElementById('article-container');
    const postsPerPage = 5;
    let currentPage = 1;

    function renderBlogPostsList(page) {
        const startIndex = (page - 1) * postsPerPage;
        const endIndex = startIndex + postsPerPage;
        const postsToShow = blogPosts.slice(startIndex, endIndex);

        blogPostsContainer.innerHTML = '';
        postsToShow.forEach((post, index) => {
            const postElement = document.createElement('div');
            postElement.className = 'blog-post-item';
            postElement.innerHTML = `
                <h2>${post.title}</h2>
                <div class="blog-post-meta">Posted on ${post.date}</div>
                <button class="read-more" data-index="${startIndex + index}">Read More</button>
            `;
            blogPostsContainer.appendChild(postElement);
        });

        // Add pagination controls
        const paginationElement = document.createElement('div');
        paginationElement.className = 'pagination';
        paginationElement.innerHTML = `
            ${page > 1 ? '<button class="prev-page">Previous</button>' : ''}
            ${endIndex < blogPosts.length ? '<button class="next-page">Next</button>' : ''}
        `;
        blogPostsContainer.appendChild(paginationElement);

        // Add event listeners for pagination
        const prevButton = paginationElement.querySelector('.prev-page');
        const nextButton = paginationElement.querySelector('.next-page');
        if (prevButton) prevButton.addEventListener('click', () => renderBlogPostsList(page - 1));
        if (nextButton) nextButton.addEventListener('click', () => renderBlogPostsList(page + 1));

        // Add event listeners for "Read More" buttons
        const readMoreButtons = document.querySelectorAll('.read-more');
        readMoreButtons.forEach(button => {
            button.addEventListener('click', function() {
                const index = parseInt(this.getAttribute('data-index'));
                renderFullArticle(blogPosts[index]);
            });
        });
    }

    function renderFullArticle(post) {
        console.log("Rendering full article:", post.title);
        blogPostsContainer.style.display = 'none';
        articleContainer.style.display = 'block';
        articleContainer.innerHTML = `
            <h1>${post.title}</h1>
            <div class="article-meta">Posted on ${post.date}</div>
            <div class="article-content">${post.content}</div>
            <button class="back-to-list">Back to Blog List</button>
            <div class="article-comments">
                <h3>Comments</h3>
                <ul class="comments-list">
                    ${post.comments.map(comment => `<li>${comment}</li>`).join('')}
                </ul>
                <form class="comment-form">
                    <textarea placeholder="Leave a comment"></textarea>
                    <button type="submit">Post Comment</button>
                </form>
            </div>
        `;

        const backButton = articleContainer.querySelector('.back-to-list');
        backButton.addEventListener('click', () => {
            articleContainer.style.display = 'none';
            blogPostsContainer.style.display = 'block';
        });

        const commentForm = articleContainer.querySelector('.comment-form');
        commentForm.addEventListener('submit', function(e) {
            e.preventDefault();
            const commentText = this.querySelector('textarea').value;
            if (commentText) {
                post.comments.push(commentText);
                renderFullArticle(post);
            }
        });
    }

    renderBlogPostsList(currentPage);
